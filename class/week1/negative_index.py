# subscripting, python allows for negative indexes also!
str = "Hello"
index = -4
print(str[index])

print(f"{str}[{index}]={str[index]}")

# String concatenation, not summation
print("123" + "456")

# mathematical Integer
print(123 + 456)
# large numbers with comma separator use under_bar
print(342_654_789)

# Float
print(3.1459)

# Boolean
print(True)
print(False)